/* Program for Patter
 *  12345
	12345
	12345
	12345
	12345
*/

public class Pattern5
{
    public static void main(String arg[]) {
   
        for (int i = 1; i <= 5; i++) {
            for (int j = 1; j <= 5; j++) {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}