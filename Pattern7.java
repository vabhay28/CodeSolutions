/* Program for Patter
 *  54321
	54321
	54321
	54321
	54321
*/

public class Pattern7
{
    public static void main(String arg[]) {
     
        for (int i = 1; i <= 5; i++) {
            for (int j = 5; j > 0; j--) {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}